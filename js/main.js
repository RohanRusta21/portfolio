;(function () {
	
	'use strict';

	var isMobile = {
		Android: function() {
			return navigator.userAgent.match(/Android/i);
		},
			BlackBerry: function() {
			return navigator.userAgent.match(/BlackBerry/i);
		},
			iOS: function() {
			return navigator.userAgent.match(/iPhone|iPad|iPod/i);
		},
			Opera: function() {
			return navigator.userAgent.match(/Opera Mini/i);
		},
			Windows: function() {
			return navigator.userAgent.match(/IEMobile/i);
		},
			any: function() {
			return (isMobile.Android() || isMobile.BlackBerry() || isMobile.iOS() || isMobile.Opera() || isMobile.Windows());
		}
	};

	var contactForm = function() {
		$('#contact-form').on('submit', function(event) {
			event.preventDefault(); // prevent reload
			
			// Show loading state
			var submitBtn = $(this).find('input[type="submit"]');
			var originalText = submitBtn.val();
			submitBtn.val('Sending...').prop('disabled', true);
			
			// Add current time to the form as a hidden field
			var now = new Date();
			var timeString = now.toLocaleString();
			
			// Create a hidden input for time if it doesn't exist
			if (!$('#time').length) {
				$(this).append('<input type="hidden" id="time" name="time">');
			}
			$('#time').val(timeString);
			
			// Combine first and last name
			var fullName = $('#fname').val() + ' ' + $('#lname').val();
			if (!$('#full_name').length) {
				$(this).append('<input type="hidden" id="full_name" name="name">');
			}
			$('#full_name').val(fullName);
			
			// Send form using EmailJS
			emailjs.sendForm('service_dc4p9ys', 'template_qojiyuh', this)
				.then(function() {
					alert('Your message has been sent successfully! I will get back to you soon.');
					$('#contact-form')[0].reset(); // Reset form
					submitBtn.val(originalText).prop('disabled', false);
				}, function(error) {
					alert('Oops... Failed to send message. Please try again or contact me directly at rohanrustagi21@gmail.com');
					console.error('EmailJS Error:', error);
					submitBtn.val(originalText).prop('disabled', false);
				});
		});
	}; 

	var fullHeight = function() {

		if ( !isMobile.any() ) {
			$('.js-fullheight').css('height', $(window).height());
			$(window).resize(function(){
				$('.js-fullheight').css('height', $(window).height());
			});
		}
	};

	// Parallax
	var parallax = function() {
		$(window).stellar();
	};

	var contentWayPoint = function() {
		var i = 0;
		$('.animate-box').waypoint( function( direction ) {

			if( direction === 'down' && !$(this.element).hasClass('animated-fast') ) {
				
				i++;

				$(this.element).addClass('item-animate');
				setTimeout(function(){

					$('body .animate-box.item-animate').each(function(k){
						var el = $(this);
						setTimeout( function () {
							var effect = el.data('animate-effect');
							if ( effect === 'fadeIn') {
								el.addClass('fadeIn animated-fast');
							} else if ( effect === 'fadeInLeft') {
								el.addClass('fadeInLeft animated-fast');
							} else if ( effect === 'fadeInRight') {
								el.addClass('fadeInRight animated-fast');
							} else {
								el.addClass('fadeInUp animated-fast');
							}

							el.removeClass('item-animate');
						},  k * 100, 'easeInOutExpo' );
					});
					
				}, 50);
				
			}

		} , { offset: '85%' } );
	};



	var goToTop = function() {

		$('.js-gotop').on('click', function(event){
			
			event.preventDefault();

			$('html, body').animate({
				scrollTop: $('html').offset().top
			}, 500, 'easeInOutExpo');
			
			return false;
		});

		$(window).scroll(function(){

			var $win = $(window);
			if ($win.scrollTop() > 200) {
				$('.js-top').addClass('active');
			} else {
				$('.js-top').removeClass('active');
			}

		});
	
	};

	var pieChart = function() {
		$('.chart').easyPieChart({
			scaleColor: false,
			lineWidth: 4,
			lineCap: 'butt',
			barColor: '#FF9000',
			trackColor:	"#f5f5f5",
			size: 160,
			animate: 1000
		});
	};

	var skillsWayPoint = function() {
		if ($('#fh5co-skills').length > 0 ) {
			$('#fh5co-skills').waypoint( function( direction ) {
										
				if( direction === 'down' && !$(this.element).hasClass('animated') ) {
					setTimeout( pieChart , 400);					
					$(this.element).addClass('animated');
				}
			} , { offset: '90%' } );
		}

	};


	// Loading page
	var loaderPage = function() {
		$(".fh5co-loader").fadeOut("slow");
	};

	
	$(function(){
		contentWayPoint();
		goToTop();
		loaderPage();
		fullHeight();
		parallax();
		// pieChart();
		skillsWayPoint();
		contactForm();
	});


}());